import fs from 'fs';
import path from 'path';

// Paths
const kanjiFilePath = path.join('src', 'data', 'kanji.js');
const radicalsFilePath = path.join('src', 'data', 'radicals.js');
const outputFilePath = path.join('src', 'data', 'components.js');

// Load databases
console.log('Loading kanji and radicals databases...');
const kanjiFileContent = fs.readFileSync(kanjiFilePath, 'utf-8');
const jsonKanjiStr = kanjiFileContent.substring(kanjiFileContent.indexOf('['), kanjiFileContent.lastIndexOf(']') + 1);
const kanjiData = JSON.parse(jsonKanjiStr);

const radicalsFileContent = fs.readFileSync(radicalsFilePath, 'utf-8');
const jsonRadicalsStr = radicalsFileContent.substring(radicalsFileContent.indexOf('['), radicalsFileContent.lastIndexOf(']') + 1);
const radicalsData = JSON.parse(jsonRadicalsStr);

console.log(`Loaded ${kanjiData.length} curriculum kanjis.`);
console.log(`Loaded ${radicalsData.length} radicals.`);

function toHiragana(str) {
  if (!str) return '';
  return str.replace(/[\u30a1-\u30f6]/g, (match) => {
    return String.fromCharCode(match.charCodeAt(0) - 0x60);
  });
}

const dbChars = new Set([
  ...kanjiData.map(k => k.kanji),
  ...radicalsData.map(r => r.kanji)
]);

async function run() {
  try {
    console.log('Fetching visual decomposition mapping from scriptin/topokanji...');
    const res = await fetch('https://raw.githubusercontent.com/scriptin/topokanji/master/dependencies/1-to-N.json');
    if (!res.ok) {
      throw new Error(`Failed to fetch decomposition: ${res.statusText}`);
    }
    const topoDecomp = await res.json();
    console.log('Visual decomposition mapping loaded.');

    // 1. Gather all direct components for our 300 curriculum kanjis
    const uniqueComps = new Set();
    const finalDecompositions = {};

    for (const item of kanjiData) {
      const char = item.kanji;
      const comps = topoDecomp[char] || [];
      // Filter out '0' which indicates no further decomposition in topokanji
      const cleanComps = comps.filter(c => c !== '0');
      if (cleanComps.length > 0) {
        finalDecompositions[char] = cleanComps;
        cleanComps.forEach(c => uniqueComps.add(c));
      }
    }

    console.log(`Found ${Object.keys(finalDecompositions).length} kanjis with decompositions.`);
    console.log(`Total unique component characters found: ${uniqueComps.size}`);

    // 2. Determine which components are missing from our local kanji & radicals databases
    const missingComps = [];
    for (const comp of uniqueComps) {
      if (!dbChars.has(comp)) {
        missingComps.push(comp);
      }
    }
    console.log(`Found ${missingComps.length} component characters missing from our databases.`);

    // 3. Fetch missing components from kanjiapi.dev
    const externalComponents = {};
    let fetchedCount = 0;

    for (const comp of missingComps) {
      fetchedCount++;
      console.log(`[${fetchedCount}/${missingComps.length}] Fetching metadata for missing component: ${comp}`);
      try {
        const res = await fetch(`https://kanjiapi.dev/v1/kanji/${encodeURIComponent(comp)}`);
        if (res.ok) {
          const apiData = await res.json();
          externalComponents[comp] = {
            kanji: comp,
            meaning: apiData.meanings.slice(0, 3).join(', '),
            reading: [...apiData.kun_readings, ...apiData.on_readings].map(toHiragana).join(' / '),
            type: 'external_kanji'
          };
        } else {
          // Fallback if not found in kanjiapi.dev
          externalComponents[comp] = {
            kanji: comp,
            meaning: 'Component / Radical',
            reading: 'n/a',
            type: 'unknown_component'
          };
        }
      } catch (e) {
        console.error(`Error fetching ${comp}:`, e.message);
        externalComponents[comp] = {
          kanji: comp,
          meaning: 'Component / Radical',
          reading: 'n/a',
          type: 'unknown_component'
        };
      }
      // Delay to avoid spamming the server
      await new Promise(r => setTimeout(r, 80));
    }

    // 4. Build componentsDb mapping
    const componentsDb = {};

    // Map curriculum kanjis
    kanjiData.forEach(item => {
      componentsDb[item.kanji] = {
        id: item.id,
        meaning: item.meaning,
        reading: item.reading,
        type: 'kanji'
      };
    });

    // Map radicals
    radicalsData.forEach(item => {
      componentsDb[item.kanji] = {
        id: `r${item.id}`,
        meaning: item.meaning.replace(/^\[Radical\]\s*/, ''),
        reading: item.reading,
        type: 'radical'
      };
    });

    // Map external components fetched from api
    Object.assign(componentsDb, externalComponents);

    // Write back to components.js
    const outputContent = `// Automatically generated by generate-decompositions.js. Do not edit directly.

export const componentsDb = ${JSON.stringify(componentsDb, null, 2)};

export const kanjiDecompositions = ${JSON.stringify(finalDecompositions, null, 2)};
`;

    fs.writeFileSync(outputFilePath, outputContent);
    console.log(`Successfully generated and wrote components database to ${outputFilePath}!`);

  } catch (error) {
    console.error('Execution failed:', error);
  }
}

run();
